
    
<a> <b>Zodpovědnost za výživu</b> - Prakticky všichni víme, že výživa psa je nejdůležitější položka v péči o naše psy. Nicméně část chovatelů a majitelů psů stále ještě zodpovědnost za výživu (a tím za zdraví) svého psa přenechává výrobcům krmiv pro psy. K radosti našich psů (a ve prospěch jejich zdraví) je však kromě diskusí „kterou značku granulí“ také stále častěji předmětem jaký způsob výživy je pro zdraví psa (a dlouhý život bez nemocí) to nejlepší. </a>
<br>
<br>
<a><b>Varianty výživy</b> - Mezi zastánci toho či onoho způsobu krmení psa se pak hovoří o variantách výživy: pouze granule, pouze nebo občas konzervy, vaření masa s přílohou, přebytky z kuchyně nebo přerůzné kombinace. Poslední dobou se začíná komunikovat také varianta syrové výživy BARF (syrové maso, syrové kosti, syrová zelenina).</a>



