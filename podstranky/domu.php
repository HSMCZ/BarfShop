<a><b>BARF</b> je zkratka, která se dá přeložit mnoha způsoby = Born Again Raw Feeders (znovuzrozen krmit syrově)</a>
<br>
<br>
<a><b>NEBO</b> Bones and Raw Food (kosti a syrová strana.)</a>
<br>
<br>
<a>Jedná se tedy o metodu výživy masožravých domácích zvířat, jež nahrazuje granulované, průmyslově vyráběné krmivo za to tradičnější – syrové.</a>
<a>Tato pestrá strava se zakládá na vyváženém poměru především masa a kostí a bývá doplněna různými přílohami od zeleniny, přes těstoviny až po vybrané mléčné výrobky.</a>
<a><b> BARF</b> je vhodný pro všechna plemena psů a koček bez ohledu na velikost či rasu.</a>
